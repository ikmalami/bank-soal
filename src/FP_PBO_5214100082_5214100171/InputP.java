    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        // TODO add your handling code here:
        String matakuliah   = jTextField2.getText ();
        String pokokbahasan = jTextField3.getText();
        String soal         = jTextArea1.getText();
        String jawabanA, jawabanB, jawabanC, jawabanD ;
        if (filename.equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih Direktori Terlebih Dahulu");
        } else {
            if (jTextField2.getText().equals("")||
                jTextField3.getText().equals("")||
                jTextArea1.getText().equals("")) 
            {
                JOptionPane.showMessageDialog(null, "Nama mata kuliah/pokok bahasan/soal tidak boleh kosong");
            } 
            else {
        
        if (opsiA.isSelected()) {
            jawabanA = jTextArea2.getText()+"*"; 
        } else {
            jawabanA = jTextArea2.getText()+"#"; 
        }
        if (opsiB.isSelected()) {
            jawabanB = jTextArea3.getText()+"*";
        } else {
            jawabanB = jTextArea3.getText()+"#"; 
        }
        if (opsiC.isSelected()) {
            jawabanC = jTextArea4.getText()+"*"; 
        } else {
            jawabanC = jTextArea4.getText()+"#"; 
        }
        if (opsiD.isSelected()) {
            jawabanD = jTextArea5.getText()+"*"; 
        } else {
            jawabanD = jTextArea5.getText()+"#"; 
        }
        
        datapenuh.add(new Database(matakuliah, pokokbahasan, soal, jawabanA, jawabanB, jawabanC, jawabanD));
        
        try {
            FileOutputStream fos    = new FileOutputStream(filename);
            ObjectOutputStream oos  = new ObjectOutputStream(fos);
            
            oos.writeObject(datapenuh);
            oos.flush();
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(InputP.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(InputP.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jTextField2.setText("");
        jTextField3.setText("");
        jTextArea1.setText("");
        jTextArea2.setText("");
        jTextArea3.setText("");
        jTextArea4.setText("");
        jTextArea5.setText("");
        opsiA.setSelected(false);
        opsiB.setSelected(false);
        opsiC.setSelected(false);
        opsiD.setSelected(false);
            }
        }  
    }//GEN-LAST:event_inputActionPerformed

